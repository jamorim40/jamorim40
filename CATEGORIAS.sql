/*
================DDL - Data Definition language================
Engloba os comandos para definir um banco de dados:
	*CREATE: cria objetos, por exemplo, Banco de Dados, Tabelas, etc..
	*DROP:	Exlui objetos, por exemplo, TABLE, DATABASE, etc..
	*ALTER: Altera objetos existentes, por exemplo, ALTER TABLE
	*TRUNCATE: Exclui todo conteúdo de uma tabela e redefine com seu valor inicial
	*COMMENT: Comentários, '--' ou '/* */ 


================DQL - Data Query language================
responsável por comandos de consultar dados em uma tabela
	*SELECT: Utilizado para consultar dados em uma tabela


================DML - Data Manipulayion language================
Utilizado para manipulação de dados
	*INSERT: Insere dados em uma ou mais tabelas
	*UPDATE: altualiza os dados já existentes em uma ou mais tabelas
	*DELTE: Exclui os registros de uma ou mais tabelas


================DCL - Data Control language================
Relacionado à segurança do banco de dados:
	*GRANT: Fornece a um determinado usuário o privilégio de acesso do banco de dados
	*REVOKE: Retira os privilégios de acesso de um determinado usuário

================DTL - Data Transaction language================
relacionado às transações de dados
	*BEGIN TRANSACTION: Inicia uma transação explicita 
	*COMMIT: Finaliza uma transação explicita
	*ROLLBACK: Retorna as modificações feitas enquando a transação não for finlaizada com o comnando COMMITE
*/